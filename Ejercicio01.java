public class Ejercicio01 {
    public static void main(String[] args) {
        int mayor = 100; //Ejercicio 1
        int menor = 10;

        System.out.println("La suma de ambos numeros es: " + (mayor + menor));

        System.out.println("La resta de ambos numeros es: " + (mayor - menor));

        System.out.println("La multiplicacion de ambos numero es: " + (mayor * menor));

        System.out.println("La division de ambos numero es: " + (mayor / menor));

        System.out.println("El resto de ambos numeros es: " + (mayor % menor));
    }
}
